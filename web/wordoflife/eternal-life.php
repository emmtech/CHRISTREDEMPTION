                           <p>
                                For a man to have eternal life, he must have Christ in his life, he must do the will of God through Christ Jesus. As said earlier, in the beginning of man God gave us life, but 
                                when man disobeyed God, the life of man was cut short and this disobedience brought separation between God and man. But because of the love of God upon His creation He gave us His 
                                only Son to come inform of man to die for us as a sacrifice for the sins of man. His death accomplished the forgiveness of our sins and our eternal redemption <i id="b3" style="cursor:pointer; background-color:#00F; color:#fff">Col. 1:14</i>&emsp;<label id="bible3">In whom we have redemption through his blood, even the forgiveness of sins:
</label><i id="b4" style="cursor:pointer; background-color:#00F; color:#fff">Heb. 9:12</i>.<label id="bible4"> Neither by the blood of goats and calves, but by his own blood he entered in once into the holy place, having obtained eternal redemption for us.</label>
                            </p>
                            <p>
                                The eternal life to man that conquers death; it is a resurrection and life. The Lord Jesus once told His disciples, "I am the resurrection and the life" <i id="b5" style="cursor:pointer; background-color:#00F; color:#fff">John 11:25</i><label id="bible5">Jesus said unto her, "I am the resurrection, and the life: he that believeth in me, though he were dead, yet shall he live: "</label>. He is the indestructible
                                and incorruptible life <i id="b6" style="cursor:pointer; background-color:#00F; color:#fff">Heb. 7:16</i><label id="bible6"> Who is made, not after the law of a carnal commandment, but after the power of an endless life.
</label>; <i id="b7" style="cursor:pointer; background-color:#00F; color:#fff">II Tim. 1:10</i><label id="bible7">But is now made manifest by the appearing of our Saviour Jesus Christ, who hath abolished death, and hath brought life and immortality to light through the gospel:
</label>. He is the resurrection that overcomes death. If not because of His resurrection, we wouldn't have been victorious over sin and death  <i id="b8" style="cursor:pointer; background-color:#00F; color:#fff">1Cor. 15:55-57</i><label id="bible8">O death, where is thy sting? O grave, where is thy victory? The sting of death is sin; and the strength of sin is the law. But thanks be to God, which giveth us the victory through our Lord Jesus Christ.</label>.
                            </p>
                            <p>
                               In the gospel of John, Jesus Christ tells us in plain words why He became a man and why He came to man; "I have come that they may have life and may have it abundantly" <i id="b9" style="cursor:pointer; background-color:#00F; color:#fff">John 10:10b</i><label id="bible9">I am come that they might have life, and that they might have it more abundantly.</label>. Christ certainly came to
                               die for our sins, but this was not the full cause of His coming, but His death accomplished the forgiveness of our sins and our eternal redemption <i id="b10" style="cursor:pointer; background-color:#00F; color:#fff">col. 1:14</i><label id="bible10">In whom we have redemption through his blood, even the forgiveness of sins:</label> <i id="b11" style="cursor:pointer; background-color:#00F; color:#fff">Heb. 9:12</i><label id="bible11"> Neither by the blood of goats and calves, but by his own blood he entered in once into the holy place, having obtained eternal redemption for us.</label>. 
                            </p>
                            <p>
                                The book Colossians written by Paul to the Colossians tells the work of Christ in the entire world. Through Christ Jesus, we were drawn back to God, by making us pure, faultless and holy into His presence <i id="b12" style="cursor:pointer; background-color:#00F; color:#fff">Col. 1:20-21</i><label id="bible12">And you, that were sometime alienated and enemies in your mind by wicked works, yet now hath he reconciled. In the body of his flesh through death, to present you holy and unblameable and unreproveable in his sight:
</label>. 
                                Through Christ our sins are forgiven and makes us victorious and regenerate us with eternal life <i>1 Peter 1:3</i>. If we believe in Him and accept His salvation, we will enjoy the eternal life of God <i id="b13" style="cursor:pointer; background-color:#00F; color:#fff">John 3:16, 20:31</i><label id="bible13">"For God so loved the world, that he gave his only begotten Son, that whosoever believeth in him should not perish, but have everlasting life."(John 3:16)<br />But these are written, that ye might believe that Jesus is the Christ, the Son of God; and that believing ye might have life through his name.(John 3:20-21)

</label>. 
                                It is a when I hear people say, there is no life after death. They say that because they are not in Christ, "but if Christ is in you, then even though your body is subject to death because of sin, the Spirit gives life because 
                                of righteousness <i id="b14" style="cursor:pointer; background-color:#00F; color:#fff">Rom. 8:10</i>"<label id="bible14">And if Christ be in you, the body is dead because of sin; but the Spirit is life because of righteousness.</label>. This simply means there is eternal life only in Christ Jesus. 
                            </p>
                            <p>
                                There is life, happiness, hope, joy, salvation and redemption in Christ Jesus. Salvation is free and the only things you need to do, is to surrender your soul and soak yourself in His blood. Do not postpone your salvation, 
                                procrastination is too dangerous. Strife to be a beneficiary of eternal life (kingdom of God), where there is no gnashing of teeth, crying and sorrow.
                                <b><i>...Remember Jesus Christ is the only way to eternal life</i>.</b>
                            </p>
