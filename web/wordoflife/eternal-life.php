                            <p>
                                The world is a cosmos of commodities. Daily we are engaged in so many activities, to find or meet daily needs. Nations shore up the value of their economies with stock piles 
                                of gold, diamond and other resources; individuals amass wealth by their clever manipulation of things. But all this is in vain. 
                                Regardless of how many commodities we possess, how much wealth we amass, ultimately our lives fail and we pass away. Life is what precious and valuable on this earth, and 
                                there is no market for it and no amount of wealth you amass can keep it. Sadly, the supply of life diminishes for each of us, and each of us on a certain day will die.
                            </p>
                            <p>
                                God alone gives life. All creation testifies that He is a God of life, and He freely gives life to His creation. Everything is succumbs to death, only God Himself is eternally 
                                living for God's life is eternal. God's life is eternal in nature, quality, and quantity, in space, and in time; it is without beginning without end, and without limitation, as 
                                God Himself is. What we need most is eternal life, and we can only have this through Christ Jesus, our Lord, God's only Son. Every of the wealth, possessions we amass are of no 
                                value without God's eternal life. God in His infinite mercy is ready to give man His eternal life. God the Spirit has embodied Himself in Christ and made available through the 
                                Spirit to all who believe in Him. 
                            </p>
