                            <h3 class="media" style="color:#F00;">The Power of Praise</h3>
                           <p>I greet you all in the name of God the Father, The Son, and The Holy Spirit. I pray if Jesus tarry we shall celebrate more Sunday on earth in Jesus name.</p>
 <p>It was discovered that so many Christians don't understand what praising God is and what it could do. Some see it as something I can just do anyhow. Some also think that some people are gifted to praise God. Some also see it as a means of whiling away time.</p>
<p>Praising God is far beyond singing and dancing but it is the connection of the Holy Spirit and losing our earthly body. When you praise God or praising God, you forget yourself and you'll be taking over by the Holy Spirit. You will lost yourself in the Spirit.
Praising God is not a gift but it should be a mandate. Both old and young must learn how to praise God, even more than we expected. Praise was mentioned in the Bible approximately 340 times. Have you praised Him today? Do you always praise Him? And how are you praising Him?
Note: Remember, when you praise God then you are worshipping him.</p>

 <h5><b>Outline 1: HOW CAN I PRAISE GOD?</b><i id="b1" style="cursor:pointer; background-color:#00F; color:#fff">Joel 2:1</i><label id="bible1">Blow ye the trumpet in Zion, and sound an alarm in my holy mountain: let all the inhabitants of the land tremble: for the day of the LORD cometh, for it is nigh at hand; 
</label>; <i id="b2" style="cursor:pointer; background-color:#00F; color:#fff">2 Samuel 6:14</i><label id="bible2">And David danced before the LORD with all his might; and David was girded with a linen ephod.</label>;<i id="b3" style="cursor:pointer; background-color:#00F; color:#fff">Psalm 86:12</i><label id="bible3"> I will praise thee, O Lord my God, with all my heart: and I will glorify thy name for evermore.
</label></h5>
<p>There is no fixed way of praising God. But you must praise Him with everything that you have (wholeheartedly and Might). You can praise Him with your hands, legs, body, trumpet, jumping, rolling on the floor, voice, kneeling, standing, waving your hands and many more. You have to feel embarrass by praising GOD. </p>

<h5><b>Outline 2: WHEN CAN I PRAISE GOD?</b></h5>
<ul>
    <li>Every day, Every night and Every moment <i id="b4" style="cursor:pointer; background-color:#00F; color:#fff">Psalm 34:1</i><label id="bible4"></label>; <i id="b5" style="cursor:pointer; background-color:#00F; color:#fff">Hebrew 13:15</i><label id="bible5"></label>; <i id="b6" style="cursor:pointer; background-color:#00F; color:#fff">Psalm 113:3</i><label id="bible6"></label></li>
    <li>For being alive  <i id="b6" style="cursor:pointer; background-color:#00F; color:#fff">Psalm 146:2</i>,<label id="bible6"></label> <i id="b7" style="cursor:pointer; background-color:#00F; color:#fff">Psalm 150:6</i><label id="bible7"></label></li>
    <li>In times of trouble, war and sorrow  <i id="b8" style="cursor:pointer; background-color:#00F; color:#fff">Acts 16:25-26 </i>(Paul & Silas); <label id="bible8"></label><i id="b9" style="cursor:pointer; background-color:#00F; color:#fff">2Samuel 20:12 </i>(David's son died);<label id="bible9"></label> <i id="b10" style="cursor:pointer; background-color:#00F; color:#fff">2Chronicles 20:6, 7, 22(the war of Edom against Jehoshaphat)</i>.<label id="bible10"></label></li>
</ul>

<h5><b>Outline 3: WHY DO WE NEED TO PRAISE GOD?</b></h5>
We have so much to praise God for, there's great power in giving honour to Him. We must give Him praise because of:
<ul>
    <li>His mightiness and greatness. <i id="b11" style="cursor:pointer; background-color:#00F; color:#fff">Psalm 150:2</i><label id="bible11"></label>; <i id="b12" style="cursor:pointer; background-color:#00F; color:#fff">Ex. 14:17</i><label id="bible12"></label></li>
    <li>His mercy <i id="b13" style="cursor:pointer; background-color:#00F; color:#fff">Psalm 118</i><label id="bible13"></label></li>
    <li>His protection and provision <i id="b14" style="cursor:pointer; background-color:#00F; color:#fff">Exodus 14:19-29</i><label id="label14"></label></li>
    <li>His awesomeness</li>
    <li>His great love <i id="b15" style="cursor:pointer; background-color:#00F; color:#fff">John 3:16</i><label id="bible15"></label></li>
    <li>His indescribable gift. <i id="b16" style="cursor:pointer; background-color:#00F; color:#fff"> 2Corinthians 9:15</i><label id="bible16"></label></li>
</ul>
<h5><b>Outline 4: WHAT PRAISING GOD CAN DO?</b></h5>
<ul>
    <li>Praise brought down the presence of God <i id="b1" style="cursor:pointer; background-color:#00F; color:#fff">2 Samuel 6:4-5</i>;<label id="17"></label> <i id="b18" style="cursor:pointer; background-color:#00F; color:#fff">Psalm 22:3</i><label id="18"></label></li>
    <li>Praise brings us to a place of humility  <i id="b19" style="cursor:pointer; background-color:#00F; color:#fff">Psalm 95:2-3;</i><label id="19"></label> <i id="b19" style="cursor:pointer; background-color:#00F; color:#fff">Psalm 35:18</i><label id="19"></label></li>
    <li>Praise makes enemy flee <i id="b20" style="cursor:pointer; background-color:#00F; color:#fff">2Chronicles 20:22</i><label id="bible20"></label></li>
    Praise makes room for God's blessing over our lives  <i id="b21" style="cursor:pointer; background-color:#00F; color:#fff">Psalm 100:4; Ephesians 1:3</i><label id="bible21"></label>
    Our Spirits are refreshed and Renewed in His presence. <i id="b22" style="cursor:pointer; background-color:#00F; color:#fff">Psalm 16:11</i><label id="bible22"></label>
