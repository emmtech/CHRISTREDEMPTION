        <p>
                    The Church &apos;Cherubim & Seraphim&apos; was formed in Heaven and entrusted into the hands of late our father St. Moses Orimolade Tunolase. 
                    His father Tunolase was from Oshodi compound at Okorun Street in Ikare Akoko District. Tunolase was from the ruling house of Sakunmi in Okorun quarters in Ikare. 
                    His mother was Odijorotan Omohewo from Edo of Ido Malara, but later became Abigael when Baptized.
                </p>
                <p>
                    Moses Orimolade was named Orimolade Okejebu. Like most religious founders a lot of mysteries surrounded his pregnancy period, the birth and childhood of Okejebu, 
                    sometimes like the foetus (Moses Orimolade) of Okejebu  complaining of being suffocated when his mother was cooking, 
                    like the foetus (Moses Orimolade) assisting the mother to lift firewood etc.
                </p>
                <p>
                    When he was eventually born, he rose to walk the same day, while Dr. Akinyele Oloyajowo said the woman who helped the mother in labor pressed him down as the child seems to wanting to lift himself up. 
Spiritual Elders claimed that it was his father being an IFA ORACLE that recited incantation (EWURE NI BI MO TI E TI RIN LOJO, AGUNTAN NI BI MO TI E TI RIN LOOJO NIBO LABAMI OMO TI WA) what is sure is 
that he attempted to walk the day he was born but some forces were used to paralyse him and so he became crippled. He was born on Sunday 11th day of November, 1877.
                </p>
                <p>
                    It is important to mention the incidence of Moses Orimolade being alone in the only Church then  i. e. CMS Mission Church under Rev. J. K. Ajayi Ajagbe singing. 
The vicar found the Church highly illuminated and heard voices of many people singing melodious tunes. These was no services and nobody got permission to use the 
Church to find the lame man alone singing as if it were many people. The Reverend requested Okejebu to teach the Church members the melodious songs he was singing 
the previous night. Unfortunately, while he was teaching them the music, he perceived the things of Heaven but the chorister were looking at his physical disabilities 
and so they could not comprehend. It is this embarrassment that resulted into Moses Orimolade&apos;s Prayer over his disability. In his dream, he was given a ROD INSIGNA and 
a CROWN. ROD is for victory, INSIGNA is for power of prayer and speech while CROWN stood for honors and multi respect of every individual.
                </p>
                <p>
                   This gave him the indication that it was time to start spreading the Gospel of Jesus Christ. There can be no mission without vision and no vision without mission. 
His mission was principally on Evangelism and Healing. His people were idol, Oro worshippers and masqueraders. He converted many souls for Christ and later became 
a threats to the occultists because it became difficult for them to eat. He healed people with the power of prayer without any personal or monetary gratification. 
He left Ikare for many cities, towns and villages but later came to Agosofin in Lagos where he met Captain Christiana Abiodun Akinsowon &minus; an African woman who went 
into trance at the age of 18.  Captain Chritiana Abiodun Emmanuel was born on Christmas day 1907 in Ajase Port-Novo Republic of Benin. Her father O.B.A
Akinsowon hailed from a royal family in Abeokuta in Nigeria, while her mother E.Z. Akinsowon was a princess of a royal family in Port-Novo.
                </p>
                <p>
                    Captain Christiana Abiodun Akinsowon had her education in Lagos, and she trained as 
a fashion designer and became an established textile trader. Captain Abiodun on 
Thursday June 18th, 1925 experienced an encounter with an angel during the annual 
Roman Catholic Church celebration of Corpus Christi that changed her entire life. 
Her subsequent 21 days trance immediately after this encounter culminated in her 
contact and association with Baba Aladura Late St. Moses Orimolade Tunolase. 
The duo by devine directive established the Holy order of Cherubim & Seraphim. 
It should be noted that the Angel friend that Mama Captain Abiodun encountered 
with taught her prayers for healing and for blessing of water.
                </p>
                <p>
                    The members were fellowshipping with St. Moses Orimolade when he discovered that the 
fellowship may die a natural death unless they belong to an organized society. He asked 
members to go for three (3) days fasting and prayers for God&rsquo;s divine direction. During 
this period, revelation were revealed through vision from different areas of Nigeria by 
visionists. There were conflicted reports as to who the letters <b>SE</b> was revailed through, 
Omoyajowo claimed that it was a woman; Elder Olu Coker recorded that it was Alagba (Elder) 
Soji Adefolu. Another member saw vision that revailed the letters RA and a United Native 
African Church Clergyman Rev. J.C. Bar from Ode Irele in Okitipupa interpreted the two i.e. 
<b>SE,RA</b> to mean <b>SERAFU (Seraph)</b> on the 9th day of September 1925. In 1927, Madam Rebecca Johnson 
alias Iya Ebun Dreamt and was told that Seraphs are not alone in Heaven and that they are TWINS 
that cannot be separated. So the name CHERUB was added and so the Heavenly Society CHERUBIM & SERAPHIM was born.
                </p>
                <p>
                    The Cherubim and Seraphim Church was founded for its time to preach the Gospel of our 
Lord Jesus Christ; the spreading of the good news of the Saving Grace of our Lord Jesus 
Christ which comes from repentance from sins and the acceptance of Jesus Christ as our 
Lord and Savior. It preached righteous living and the worship of only one true God. 
This was the vision of the Cherubim and Seraphim Church. The import of this Vision can 
only be appreciated when the founding time and circumstances are taken into consideration. 
It was a period of war and pestilence, the founding Vision was the only relevant one for that period.
                </p>
                <p>What is noted about the Cherubim & Seraphim of yesterday include:</p>
                <ul>
                    <li>Unity</li>
                    <li>Evangelism&minus;noted through the songs:  &apos;A palase lagbala orun pe kegbe yi ma bisi&hellip;&apos;</li>
                    <li>Healing</li>
                    <li>Deliverance using prayer and in the name of Jesus, noted through the song</li>
                </ul>
                <p>
                    Gidigbo Gidigbo e ya, ap&rsquo;awon aje ni ja, ati sonponna bab ni oruko Oluwa.
                    &lsquo;Onisegun nla wa nihin Jesu a ba ni daro&hellip;&rsquo;
They wear white garments. This is as recorded by Dr. Akinyele Omoyajowo
They condemned Juju practices and revealed the power of prayer through believer in the Lord Jesus Christ.
It was a congregation of Holy people
They were convinced it is the Church that will be the last. This is noted through the song &minus;&lsquo;Kinni yio kehin aye, Kerubu pelu Serafu&hellip;&rsquo;. The spiritual father did not deviate from God&rsquo;s instruction as he followed strictly the teaching of Jesus Christ. This is noted as in the song &minus; &lsquo;Ipinle ti Jesu fi lele leyi ti baba Aladura nto, keda maser o pe o ye kuro nibe o duro le Kristi apata.&rsquo;

                </p>
